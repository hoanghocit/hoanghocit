#include<stdio.h>
int main(){
	//Khai Bao Bien Gio Hien Tai
	int time = 10;
	printf("Thoi Gian Hien Tai La %d Gio \n", time);
	//Khai Bao Bien Nam
	int year;
	year = 2024;
	printf("Nam Nay La Nam %d \n", year);
	year = 2024 + 1;
	printf("Nam Sau La Nam %d \n", year);
	//Khai Bao Ky Tu
	char name = 'a';
	printf("Ky Tu La %c \n", name);
	//Khai Bao Bien Float
	float pi = 3.14;
	printf("Pi: %f", pi);
return 0;
}
